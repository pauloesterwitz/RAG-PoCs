"""Synthesize the evaluation 'goldens' (gold chunks → question + reference
answer) with DeepEval's Synthesizer, judged/generated by the local Ollama model.

Each golden's `context` is the gold chunk(s) it was generated from — that is the
gold-chunk ground truth the retrieval metrics are scored against."""
from __future__ import annotations

import hashlib
import json
import random
from collections import defaultdict
from pathlib import Path

from deepeval.synthesizer import Synthesizer
from deepeval.synthesizer.config import StylingConfig, FiltrationConfig

from ..config import SETTINGS, EVAL_DIR
from ..indexer import load_base_index
from ..store import BaseIndex
from .deepeval_models import OllamaJudge

GOLDENS_FILE = EVAL_DIR / "goldens.json"

_STYLING = StylingConfig(
    scenario=(
        "A technical reader studying a corpus of AI/ML and software-engineering "
        "research papers and book chapters, asking focused factual questions."
    ),
    task=(
        "Answer specific questions grounded in the source passages about concepts, "
        "methods, definitions, trade-offs and findings."
    ),
    input_format="A clear, self-contained question answerable from the passage.",
    expected_output_format="A concise, factual answer grounded only in the passage.",
)


def _ctx_key(ctx: list[str]) -> str:
    return hashlib.sha1("||".join(ctx).encode()).hexdigest()[:16]


def _select_contexts(index: BaseIndex, n: int) -> tuple[list[list[str]], list[str], dict]:
    """Round-robin across documents; each context = a chunk + its same-doc neighbour.
    Returns (contexts, source_files, ctx_key -> [chunk_ids])."""
    by_doc: dict[str, list[int]] = defaultdict(list)
    for i, c in enumerate(index.chunks):
        if c.char_count >= 600:  # skip sparse/figure chunks
            by_doc[c.doc].append(i)
    rng = random.Random(42)
    for lst in by_doc.values():
        rng.shuffle(lst)

    contexts: list[list[str]] = []
    sources: list[str] = []
    key_to_ids: dict[str, list[str]] = {}
    docs = list(by_doc.keys())
    pos = {d: 0 for d in docs}
    while len(contexts) < n:
        progressed = False
        for d in docs:
            lst = by_doc[d]
            if pos[d] >= len(lst):
                continue
            i = lst[pos[d]]
            pos[d] += 1
            progressed = True
            chunk = index.chunks[i]
            ids = [chunk.id]
            texts = [chunk.text]
            # add same-doc neighbour (next chunk index in original order if same doc)
            if i + 1 < len(index.chunks) and index.chunks[i + 1].doc == chunk.doc:
                texts.append(index.chunks[i + 1].text)
                ids.append(index.chunks[i + 1].id)
            contexts.append(texts)
            sources.append(chunk.doc)
            key_to_ids[_ctx_key(texts)] = ids
            if len(contexts) >= n:
                break
        if not progressed:
            break
    return contexts, sources, key_to_ids


def synthesize_goldens(num: int | None = None, progress=None) -> dict:
    num = num or SETTINGS.eval_num_goldens
    index = load_base_index(refresh=True)
    # oversample contexts; filtration may drop some
    contexts, sources, key_to_ids = _select_contexts(index, int(num * 1.4) + 2)
    if progress:
        progress(f"Synthesizing from {len(contexts)} gold contexts using {SETTINGS.judge_model}…")

    synth = Synthesizer(
        model=OllamaJudge(),
        async_mode=True,
        max_concurrent=SETTINGS.gen_concurrency,
        styling_config=_STYLING,
        filtration_config=FiltrationConfig(
            critic_model=OllamaJudge(), synthetic_input_quality_threshold=0.5, max_quality_retries=1
        ),
    )
    goldens = synth.generate_goldens_from_contexts(
        contexts=contexts,
        include_expected_output=True,
        max_goldens_per_context=1,
        source_files=sources,
    )

    out = []
    for g in goldens[:num]:
        ctx = list(g.context or [])
        out.append(
            {
                "input": g.input,
                "expected_output": g.expected_output,
                "context": ctx,
                "source_file": g.source_file,
                "gold_chunk_ids": key_to_ids.get(_ctx_key(ctx), []),
            }
        )
    payload = {
        "judge_model": SETTINGS.judge_model,
        "num_goldens": len(out),
        "goldens": out,
    }
    GOLDENS_FILE.write_text(json.dumps(payload, indent=2))
    if progress:
        progress(f"Synthesized {len(out)} goldens → {GOLDENS_FILE.name}")
    return payload


def load_goldens() -> dict | None:
    if GOLDENS_FILE.exists():
        return json.loads(GOLDENS_FILE.read_text())
    return None
